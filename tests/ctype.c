        struct test_case tc = test_cases[i];
        CHECK_TEST(tc, isalnum, retval);
        CHECK_TEST(tc, isalpha, retval);
        CHECK_TEST(tc, isascii, retval);
        CHECK_TEST(tc, isblank, retval);
        CHECK_TEST(tc, iscntrl, retval);
